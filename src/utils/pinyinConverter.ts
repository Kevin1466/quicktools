const removeTone = (pinyin: string): string => {
  const toneMap: Record<string, string> = {
    'ā': 'a', 'á': 'a', 'ǎ': 'a', 'à': 'a',
    'ē': 'e', 'é': 'e', 'ě': 'e', 'è': 'e',
    'ī': 'i', 'í': 'i', 'ǐ': 'i', 'ì': 'i',
    'ō': 'o', 'ó': 'o', 'ǒ': 'o', 'ò': 'o',
    'ū': 'u', 'ú': 'u', 'ǔ': 'u', 'ù': 'u',
    'ǖ': 'u', 'ǘ': 'u', 'ǚ': 'u', 'ǜ': 'u',
  }
  return pinyin.split('').map(char => toneMap[char] || char).join('')
}

export const textToPinyinLocal = (
  text: string,
  mode: 'tone' | 'no-tone' | 'initial' = 'tone'
): string => {
  const chars = text.split('')
  const result: string[] = []
  
  for (const char of chars) {
    const pinyin = pinyinMap[char]
    if (pinyin) {
      if (mode === 'initial') {
        result.push(pinyin[0].toUpperCase())
      } else if (mode === 'no-tone') {
        result.push(removeTone(pinyin))
      } else {
        result.push(pinyin)
      }
    } else {
      result.push(char)
    }
  }
  
  return result.join(' ')
}
