const simplifiedToTraditionalMap = Object.fromEntries(simplifiedToTraditionalEntries) as Record<string, string>
const traditionalToSimplifiedMap = Object.fromEntries(
  simplifiedToTraditionalEntries.map(([s, t]) => [t, s])
) as Record<string, string>

export const simplifiedToTraditional = (text: string) => {
  return Array.from(text)
    .map(ch => simplifiedToTraditionalMap[ch] ?? ch)
    .join('')
}

export const traditionalToSimplified = (text: string) => {
  return Array.from(text)
    .map(ch => traditionalToSimplifiedMap[ch] ?? ch)
    .join('')
}

